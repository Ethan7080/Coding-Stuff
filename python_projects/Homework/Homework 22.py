import random
while True:
    all_ = ["rock", "scissors", "paper"]
    player = input('Game start! Please enter your action: ')
    computer = "paper"
    for i in range(3):
        if player == all_[i]:
            player = all_[i]
    print("Player:", player, "vs Computer:", computer)
    if (player == "rock" and computer == "scissors") or ((player == "scissors" and computer == "paper") or (player == "paper" and computer == "rock")):
        print("Player Wins")
    elif player == computer:
        print("A tie")
    else:
        print("Computer Wins")
